// 获取所有一级分类
export const getPrimaryCategories = () => {
  return Object.keys(hierarchicalTags)
}

// 根据一级分类获取二级分类
export const getSecondaryCategories = (primaryCategory) => {
  if (hierarchicalTags[primaryCategory]) {
    return Object.keys(hierarchicalTags[primaryCategory])
  }
  return []
}

// 根据一级和二级分类获取三级标签
export const getTertiaryTags = (primaryCategory, secondaryCategory) => {
  if (hierarchicalTags[primaryCategory] && hierarchicalTags[primaryCategory][secondaryCategory]) {
    return hierarchicalTags[primaryCategory][secondaryCategory]
  }
  return []
}